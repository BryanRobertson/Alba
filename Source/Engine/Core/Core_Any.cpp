#include "Core_Precompile.hpp"
#include "Core_Any.hpp"

namespace Alba
{
	namespace Core
	{
		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		Any::Any()
		{
		}

		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		Any::Any(const Any& aCopyFrom)
		{
			if (aCopyFrom.myData.has_value())
			{
				myData = aCopyFrom.myData;
			}
		}

		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		Any::Any(Any&& aMoveFrom)
		{
			if (aMoveFrom.myData.has_value())
			{
				myData = std::move(aMoveFrom.myData);
			}
		}

		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		bool Any::IsSet() const
		{
			return myData.has_value();
		}

		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		void Any::Clear()
		{
			myData.reset();
		}
	}
}