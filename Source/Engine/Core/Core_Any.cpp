#include "Core_Precompile.hpp"
#include "Core_Any.hpp"

namespace Alba
{
	namespace Core
	{
		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		Any::Any()
		{
		}

		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		Any::Any(const Any& anOther)
			: myData(anOther.myData)
		{
		}

		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		Any::Any(Any&& anOther)
			: myData(std::move(anOther.myData))
		{
		}

		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		bool Any::IsSet() const
		{
			return myData.has_value();
		}

		//-----------------------------------------------------------------------------------------
		//-----------------------------------------------------------------------------------------
		void Any::Clear()
		{
			myData.reset();
		}
	}
}